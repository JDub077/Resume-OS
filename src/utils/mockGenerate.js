export function generateMockContent(purpose, experiences) {
  const expList = experiences.map((e, i) => {
    const idx = i + 1;
    return {
      idx,
      title: e.title,
      desc: e.description,
      result: e.result,
      tags: e.tags || [],
      category: e.category,
      time: e.time,
    };
  });

  const allTags = [...new Set(expList.flatMap(e => e.tags))].slice(0, 4);
  const tagStr = allTags.length > 0 ? allTags.join('、') : '综合素质';

  const expBlocks = expList.map(e => {
    const resultPart = e.result ? `，取得了${e.result}` : '';
    return `${e.title}（${e.category}，${e.time}）：${e.desc}${resultPart}。`;
  }).join('\n\n');

  const generators = {
    '国家奖学金': () => {
      return `尊敬的领导、老师：\n\n您好！\n\n我是XX学院XX专业的一名学生，非常荣幸能够申请本年度的国家奖学金。在过去的学习生活中，我始终以"厚德博学、求实创新"为座右铭，努力在学业成绩、科研创新、社会实践等方面实现全面发展。\n\n一、学业为先，夯实专业基础\n\n自入学以来，我始终将学业置于首位，系统学习了专业核心课程，并主动拓展知识边界。通过制定科学的学习计划和高效的时间管理，我保持了优异的学业成绩，多门专业课程达到90分以上，综合排名位于专业前列。\n\n二、实践育人，提升综合能力\n\n在注重学业的同时，我积极投身于各类实践活动，努力将所学知识转化为实际能力：\n\n${expBlocks}\n\n这些经历让我深刻体会到，真正的成长不仅来自课堂，更来自实践中的磨砺与反思。通过${tagStr}等方面的锻炼，我的综合素质得到了显著提升。\n\n三、心怀感恩，砥砺前行\n\n回首过往，每一次经历都是我成长路上的宝贵财富。国家奖学金不仅是对过去努力的肯定，更是对未来继续前行的激励。如果能够获得这份荣誉，我将以此为新的起点，更加严格要求自己，在学术研究与社会服务中发挥更大价值，努力成为德才兼备的新时代青年。\n\n感谢各位老师的审阅！\n\n此致\n敬礼！`;
    },

    '优秀学生干部': () => {
      return `尊敬的评审老师：\n\n您好！\n\n我是XX学院学生会/班级的一名学生干部，现申请"优秀学生干部"荣誉称号。任职期间，我始终以服务同学为宗旨，以务实创新为理念，在岗位上踏实工作，力求为集体创造实际价值。\n\n一、立足岗位，履职尽责\n\n在担任学生干部期间，我主要负责组织协调、活动策划与团队管理等工作。始终做到"事必躬亲、责无旁贷"，确保每一项任务落地有声。\n\n二、重点工作与成果\n\n${expBlocks}\n\n三、个人成长与反思\n\n通过${tagStr}等方面的实践锻炼，我不仅提升了个人的组织协调能力，更深刻理解了"服务"二字的分量。学生干部不是头衔，而是一份责任。未来，我将继续秉持初心，在岗位上发光发热，为同学们做实事、解难题。\n\n恳请评审老师予以肯定！`;
    },

    '优秀志愿者': () => {
      return `尊敬的评审老师：\n\n您好！\n\n我是XX学院的一名学生，现申请"优秀志愿者"荣誉称号。参与志愿服务以来，我始终秉持"奉献、友爱、互助、进步"的志愿精神，在服务他人、奉献社会的过程中收获了成长与感动。\n\n一、志愿服务经历\n\n${expBlocks}\n\n二、服务感悟\n\n每一次志愿服务都是一次心灵的洗礼。在${expList[0]?.title || '志愿服务'}中，我深刻体会到了"被需要"的幸福感。当看到服务对象脸上露出笑容时，所有的辛苦都化作了前行的动力。\n\n三、未来承诺\n\n志愿服务不是一时兴起，而是一生的追求。未来，我将继续投身公益事业，将${tagStr}等能力运用到更多志愿服务场景中，用实际行动传递温暖与希望，让青春在奉献中绽放光彩。\n\n感谢评审！`;
    },

    '求职简历': () => {
      const bullets = expList.map(e => {
        const resultPart = e.result ? `，实现${e.result}` : '';
        return `• ${e.title} | ${e.time}\n  ${e.desc}${resultPart}；`;
      }).join('\n\n');
      return `【个人经历】\n\n${bullets}\n\n【核心能力】\n\n${allTags.map(t => `• ${t}：在多项实践经历中得到充分锻炼与验证`).join('\n')}\n\n【自我评价】\n\n具备扎实的专业基础与丰富的实践经验，擅长将理论知识应用于实际场景。在团队协作中能够有效沟通、主动担当，对目标有强烈的执行力与责任感。期待在新的平台上持续学习、创造价值。`;
    },

    '网申登记表': () => {
      return `【个人优势】\n\n1. 综合素质突出：在${expList.map(e => e.category).join('、')}等多个领域积累了丰富经验。\n\n2. 实践能力扎实：\n${expList.map(e => `   • ${e.title}：${e.desc}${e.result ? '（' + e.result + '）' : ''}`).join('\n')}\n\n3. 核心能力标签：${tagStr}\n\n【职业规划】\n\n希望加入贵公司，在充分发挥个人${tagStr}等优势的基础上，与团队共同成长，为企业创造可见价值。短期目标为快速融入团队、熟悉业务；中长期目标为在领域内建立专业深度，成为能够独当一面的骨干力量。`;
    },

    '面试自我介绍': () => {
      const firstExp = expList[0];
      return `各位面试官好！\n\n我是XXX，来自XX大学XX专业。非常荣幸能够参加今天的面试。接下来，我将用两分钟时间向您介绍一下我自己。\n\n在学业方面，我始终保持对知识的热情，专业基础扎实，学习能力较强。\n\n在实践方面，我有过几段值得分享的经历：\n\n${expList.map((e, i) => `${i + 1}. ${e.title}：${e.desc}${e.result ? '，这段经历让我' + e.result.split('，')[0] + '' : ''}`).join('\n')}\n\n这些经历让我收获了${tagStr}等核心能力，也让我更加明确了自己的职业方向。\n\n我是一个做事认真负责、善于团队协作的人。如果有幸加入贵公司，我会以最快速度融入团队，用实际成果回报信任。\n\n以上就是我的自我介绍，谢谢各位面试官！`;
    },

    '保研陈述': () => {
      return `尊敬的各位老师：\n\n您好！\n\n我是XX大学XX学院XX专业的XXX，非常荣幸能够申请贵校的推荐免试研究生资格。以下是我的个人陈述。\n\n一、学业基础与专业认知\n\n本科期间，我系统学习了专业核心课程，对XX领域产生了浓厚兴趣，并持续关注该领域的前沿动态。通过课程学习与自主阅读，我建立了较为扎实的理论基础。\n\n二、科研与实践经历\n\n${expBlocks}\n\n这些经历不仅锻炼了我的${tagStr}能力，更让我对学术研究有了初步的认知与体验，坚定了继续深造的决心。\n\n三、研究兴趣与未来规划\n\n我希望在研究生阶段深入探索XX方向，将本科阶段的实践经验与理论学习相结合，力求在学术上有所突破。我相信，贵校优秀的师资力量与浓厚的学术氛围，将为我的成长提供最佳平台。\n\n恳请各位老师给予宝贵机会！\n\n此致\n敬礼！`;
    },

    '复试介绍': () => {
      return `各位老师好！\n\n我是XXX，报考贵校XX专业。非常荣幸能够进入复试环节，感谢各位老师在百忙之中审阅我的材料。\n\n一、个人基本情况\n\n我本科就读于XX大学XX专业，在校期间学业成绩稳定，专业基础较为扎实。\n\n二、实践与综合能力\n\n${expBlocks}\n\n三、读研动机与规划\n\n选择报考贵校，是因为贵校在XX领域具有卓越的学术声誉，与我个人的研究兴趣高度契合。如果有幸被录取，我计划在研一阶段夯实理论基础，研二阶段深入课题研究，争取在学术上产出有价值的成果。\n\n再次感谢各位老师，期待能够成为贵校的一员！`;
    },
  };

  const gen = generators[purpose] || generators['求职简历'];
  return gen();
}
